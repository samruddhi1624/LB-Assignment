void  Display(int iNo)
{
    if(iNo <0)
    {
        iNo =-iNo;
    }

    int icnt =0;
    for(icnt = 0; icnt<iNo; icnt++)
    {
        printf("*\t");
    }
    for(icnt = 0; icnt<iNo; icnt++)
    {
        printf("#\t");
    }
    
    printf("\n");
  
  
}

////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number :\n");
    scanf("%d",&iValue);

    Display(iValue);
    
    return 0;
}