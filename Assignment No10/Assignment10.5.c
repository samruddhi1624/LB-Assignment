double SquareMeter(int iNo)
{
   double dSquareMeter =0;
   dSquareMeter =iNo*0.0929;
   return dSquareMeter;

}
////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue =0;
    double dRet =0.0f;

    printf("Enter Distance in Squarefeet\n");
    scanf("%d",&iValue);

    dRet =SquareMeter(iValue);

    printf("%d Squarefeet is %lf SquareMeter",iValue ,dRet);

    return 0;
}