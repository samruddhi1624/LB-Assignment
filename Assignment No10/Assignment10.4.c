double FhtoCs(float fTemp)
{
   float fCelsius =0.0f;
   fCelsius =((fTemp -32)*(5.0/9.0));
   return fCelsius;
}
////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue =0.0f;
    double dRet =0.0f;

    printf("Enter temperature in Fahernheit\n");
    scanf("%f",&fValue);

    dRet =FhtoCs(fValue);

    printf("%f Farenhniets is %f Clesius",fValue ,dRet);

    return 0;
}