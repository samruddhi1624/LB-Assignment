float CircleArea(float Rad)
{
      float fAns =0.0f;
      fAns =3.14*Rad*Rad;
      
      return fAns;
}
////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue =0.0f;
    double dRet =0.0;

    printf("Enter Radius :\n");
    scanf("%f",&fValue);

    dRet =CircleArea(fValue);
    
    printf("Area Of Circle is : %f",dRet);

    return 0;
}