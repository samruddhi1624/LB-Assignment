int Factorial(int iNo)
{
    if(iNo <0)
    {
        iNo =-iNo;
    }

    int icnt =0;
    int iFact =1;

     for(icnt =1; icnt<=iNo; icnt++)
     {
          iFact = iFact*icnt;
     }

     return iFact;
  
}

////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet =0;

    printf("Enter Number :\n");
    scanf("%d",&iValue);

    iRet = Factorial(iValue);

    printf("Factorial Of %d Is %d",iValue,iRet);

    return 0;
}