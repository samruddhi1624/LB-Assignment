#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Number
//  Description :   Accept Number from user and if it is 
//                  its less than 50 print small 
//                  its greater than 50 and less than 100
//                  print medium 
//                  its greater 100 than print large
//  Author:         Samruddhi Vinayak Rupanawar
//  Date :          26/10/2025
//          
/////////////////////////////////////////////////////////////////////////////////////////////////

void Number(int iNo)
{
    if(iNo<=50)
    {
        printf("The Number Is Small\n");
    }
    else if((iNo>50)&&(iNo<100))
    {
        printf("The Number Is Medium\n");
    }
    else 
    {
        printf("The Number Is Larger\n");
    }
}
  


////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number :\n");
    scanf("%d",&iValue);

    Number(iValue);

    return 0;
}