void  Table(int iNo)
{
    if(iNo <0)
    {
        iNo =-iNo;
    }

    int icnt =0;
    int iMult =0;

     printf("Your Table is \n");
     for(icnt = 1; icnt<=10; icnt++)
    {
        iMult =icnt*iNo;
        printf("%d\t",iMult);
    }
  
  
}

