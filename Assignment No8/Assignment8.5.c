void  TableRev(int iNo)
{
    if(iNo <0)
    {
        iNo =-iNo;
    }

    int icnt =0;
    int iMult =0;

     printf("Your Table is \n");
     for(icnt = 10; icnt>=1; icnt--)
    {
        iMult =icnt*iNo;
        printf("%d\t",iMult);
    }
  
  
}

////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number :\n");
    scanf("%d",&iValue);

    TableRev(iValue);
    
    return 0;
}