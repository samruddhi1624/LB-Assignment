bool CheckEven(int iNo)
{
    if (iNo %2 ==0)
    {
        return true;
    }
    else
    {
        return false;
    }

    printf("\n");
}

////////////////////////////////////////////////////////////////////////////////////////////////
//
// Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    bool bRet = false;

    printf("Enter number :");
    scanf("%d",&iValue);

    bRet = CheckEven(iValue);

    if(bRet == true)
        {
            printf("%d is an even number",iValue);
        }
        else
        {
            printf("%d is an odd Number",iValue);
        }

    return 0;

}

